def 計算加權總成績(成績, 權重):
    總成績 = 0
    for 科目, 分數 in 成績.items():
        總成績 += 分數 * 權重[科目]
    return 總成績

def 判斷成績等第(分數):
    if 分數 >= 90:
        return 'A'
    elif 分數 >= 80:
        return 'B'
    elif 分數 >= 70:
        return 'C'
    elif 分數 >= 60:
        return 'D'
    else:
        return 'F'

def 主程式():
    科目列表 = ['國文', '英文', '數學', '自然', '社會']
    簡稱列表 = ['國', '英', '數', '自', '社']
    
    # 預設權重
    權重 = {
        '國文': 0.3,
        '英文': 0.2,
        '數學': 0.2,
        '自然': 0.2,
        '社會': 0.1
    }
    
    # 輸入成績
    while True:
        try:
            print("請依序輸入國文、英文、數學、自然、社會的成績，用空格分隔 (例如: 85 90 78 88 92)")
            成績輸入 = input("請輸入5個成績 (0-100): ").split()
            
            if len(成績輸入) != 5:
                print("請確保輸入5個成績。")
                continue
            
            成績 = {}
            for i, 分數字串 in enumerate(成績輸入):
                分數 = float(分數字串)
                if 0 <= 分數 <= 100:
                    成績[科目列表[i]] = 分數
                else:
                    raise ValueError
            
            break
        except ValueError:
            print("請確保所有輸入都是有效的數字，且在0到100之間。")
    
    # 計算加權總成績
    加權總成績 = 計算加權總成績(成績, 權重)
    
    # 決定成績等第
    成績等第 = 判斷成績等第(加權總成績)
    
    # 顯示結果
    print(f"加權總成績: {加權總成績:.2f}, 成績等第為: {成績等第}。")

# 程式執行
if __name__ == "__main__":
    主程式()